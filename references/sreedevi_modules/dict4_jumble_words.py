# Jumbled Words

x={'cta':['cat','act'],'ohme':'home','edar':['read','dare','dear'],
   'ytponh':'python','ohtmre':'mother'}
score=0
for    i,j  in   x.items() :
    print(i,":",end=" ")
    ans=input()
    if  ans  in  j :
        score+=1
print("your score is : ",score)
    


